    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (automobile.getStatus().equals("Completed")) {
            JOptionPane.showMessageDialog(null, "This Automobile has already been evaluated");
            return;
        }
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            String patientName1 = ((GarageWorkRequest) request).getAutomobileName();
            if (patientName.equals(patientName1)) {
                if (!request.getStatus().equals("Completed")) {
                    JOptionPane.showMessageDialog(null, "Diagnosis not completed for this Automobile! Evaluation Not ready");
                    return;
                }
            }
        }

        //---------------------------------------------------------------------------------------------------------------------------------------
        if ((yesBtn1.isSelected() == true && noBtn1.isSelected() == true)) {
            JOptionPane.showMessageDialog(null, "Please select only one decision!");
            return;
        }

        if (yesBtn1.isSelected() == false && noBtn1.isSelected() == false) {
            JOptionPane.showMessageDialog(null, "Please select your decision before saving!");
            return;
        }

        int dialogButton = 0;
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to save your decision", "Warning", dialogButton);
        if (dialogResult == JOptionPane.YES_OPTION) {
            boolean eligible;
            eligible = yesBtn1.isSelected();
            automobile.setAutomobile(eligible);
            automobile.setStatus("Completed");
            sendEmail(automobile.getEmailAdd(), eligible);
            JOptionPane.showMessageDialog(null, "Email sent to volunteer");
            CardLayout layout = (CardLayout) rightPanel.getLayout();
            rightPanel.remove(this);
            layout.previous(rightPanel);
            Component[] comps = this.rightPanel.getComponents();
            for (Component comp : comps) {
                if (comp instanceof ManageAutomobilesJPanel) {
                    ManageAutomobilesJPanel managePatientsJPanel = (ManageAutomobilesJPanel) comp;
                    managePatientsJPanel.disableAll();
                }
            }
            return;

        }

    }//GEN-LAST:event_jButton3ActionPerformed


