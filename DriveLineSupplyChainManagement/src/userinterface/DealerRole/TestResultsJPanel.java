    void populateDropdownTestName() {
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            String patientName1 = ((GarageWorkRequest) request).getAutomobileName();
            String testName1 = ((GarageWorkRequest) request).getTestName();
            if (patientName1.equals(this.patientName)) {
                if (!(testName1.equals("Diagnostic1") || testName1.equals("Diagnostic2") || testName1.equals("Diagnostic3")
                        || testName1.equals("Diagnostic4"))) {
                    TestName.addItem(testName1);
                }
            }
        }
    }

    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        model.setRowCount(0);
        String testName = String.valueOf(TestName.getSelectedItem());
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[3];
            String patientName1 = ((GarageWorkRequest) request).getAutomobileName();
            String testName1 = ((GarageWorkRequest) request).getTestName();
            if (testName.equals(testName1) && patientName.equals(patientName1)) {
                row[0] = request.getReceiver();
                row[1] = request.getStatus();
                String result = ((GarageWorkRequest) request).getTestResult();
                row[2] = result == null ? "Waiting" : result;
                model.addRow(row);
                System.out.println(result);
            }
        }
    }

